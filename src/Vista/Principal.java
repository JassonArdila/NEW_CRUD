    private void eliminar() {
        String id = TxtId.getText();
        if (id.equals("")) {
            JOptionPane.showMessageDialog(null, "No se puede eliminar sin seleccionar registro");
        } else {
            int opcion = JOptionPane.showConfirmDialog(null, "Â¿esta seguro de eliminar este registro?");
            String sql = "DELETE FROM users.`user` WHERE id=" + id;
            if (opcion == 0) {
                try {
                    cn = con.getConnection();
                    st = cn.createStatement();
                    st.execute(sql);
                    JOptionPane.showMessageDialog(null, "Usuario Eliminado...!!!");
                    limpiarCampos();
                    listar();
                } catch (Exception e) {
                    e.printStackTrace(System.err);
                }
            }
        }
    }
