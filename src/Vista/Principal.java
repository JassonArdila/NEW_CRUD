    void listar() {
        limpiartabla();
        String sql = "SELECT * FROM `user` order by id";
        try {
            cn = con.getConnection();
            st = cn.createStatement();
            rs = st.executeQuery(sql);
            Object[] user = new Object[4];
            modelo = (DefaultTableModel) TablaDatos.getModel();
            while (rs.next()) {
                user[0] = rs.getInt("Id");
                user[1] = rs.getString("Names");
                user[2] = rs.getString("SurNames");
                user[3] = rs.getString("DocumentNumber");
                modelo.addRow(user);
            }
            TablaDatos.setModel(modelo);
        } catch (Exception e) {
            e.printStackTrace(System.out);
        }
    }

    void Agregar() {
        String Names = TxtNames.getText();
        String SurNames = TxtSurNames.getText();
        String DocumentNumber = TxtDocument.getText();
        if (Names.equals("") || SurNames.equals("") || DocumentNumber.equals("")) {
            JOptionPane.showMessageDialog(null, "cajas estan vacias...!!!");
        } else {
            String sql = "insert into user (Names, Surnames, DocumentNumber) values('" + Names + "','" + SurNames + "','" + DocumentNumber + "')";
            try {
                cn = con.getConnection();
                st = cn.createStatement();
                st.execute(sql);
                JOptionPane.showMessageDialog(null, "Usuario Agregado...!!!");
                listar();
            } catch (Exception e) {
                e.printStackTrace(System.err);
            }
        }
    }

    void Actualizar() {
        String id = TxtId.getText();
        String Names = TxtNames.getText();
        String SurNames = TxtSurNames.getText();
        String DocumentNumber = TxtDocument.getText();
        if (Names.equals("") || SurNames.equals("") || DocumentNumber.equals("")) {
            JOptionPane.showMessageDialog(null, "cajas estan vacias...!!!");
        } else {
            String sql = "UPDATE users.`user` SET Names='" + Names + "', Surnames='" + SurNames + "', DocumentNumber='" + DocumentNumber + "' WHERE id=" + id + ";";
            try {
                cn = con.getConnection();
                st = cn.createStatement();
                st.execute(sql);
                JOptionPane.showMessageDialog(null, "Usuario Modificado...!!!");
                limpiarCampos();
                listar();
            } catch (Exception e) {
                e.printStackTrace(System.err);
            }
        }
    }

    void limpiartabla() {
        if (modelo == null) {
            return;
        }
        modelo.setRowCount(0);
    }

    void limpiarCampos() {
        TxtId.setText("");
        TxtNames.setText("");
        TxtSurNames.setText("");
        TxtDocument.setText("");
    }

